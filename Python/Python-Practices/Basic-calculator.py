# case 1: python automatically convert input into strings
number_1 = input("Enter a number: ")
number_2 = input("Enter another number: ")
result = number_1 + number_2
print(result)

# case 2: convert strings into whole number with int()
number_1 = input("Enter a number: ")
number_2 = input("Enter another number: ")
result = int(number_1) + int(number_2)
print(result)

# case 2: convert strings into number with float()
number_1 = input("Enter a number: ")
number_2 = input("Enter another number: ")
result = float(number_1) + float(number_2)
print(result)
